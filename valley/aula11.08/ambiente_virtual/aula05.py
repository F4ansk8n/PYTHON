# Fatiamento de strings
# toda string por padrão e uma lista que não saiu do armário
# ordem de tratamento
    # 0123...
    # -3210...
    # [i:f:p] = i - inicio, f - fim, p - parse

nome = "Francis Oliver Sousa de Araújo"
print(nome [15:20])
print(nome [-6:])
print(nome [0::2])
print(nome [1::2])

print(nome[2])
print(nome[-4])